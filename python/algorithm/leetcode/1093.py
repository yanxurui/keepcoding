from typing import List
class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        n = sum(count)
        sum_of_sample = sum(i*v for i,v in enumerate(count))
        mean = sum_of_sample / n
        for i in range(256):
            if count[i]:
                mi = float(i)
                break
        for i in range(255,-1,-1):
            if count[i]:
                ma = float(i)
                break
        mode = float(count.index(max(count)))
        l = (n-1)//2+1
        r = n//2+1
        cum = 0
        l_v, r_v = None, None
        for i in range(256):
            if cum < l <= cum + count[i]:
                l_v = i
            if cum < r <= cum + count[i]:
                r_v = i
                break
            cum += count[i]
        median = (l_v + r_v)/2
        return [mi, ma, mean, median, mode]


