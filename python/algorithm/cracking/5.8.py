class Render:
    def renderPixel(self, screen, x, y):
        # write code here
        i = x//8
        j = y//8
        if i == j:
            screen[i] |= ((1 << (y-x+1)) - 1) << (x%8)
        else:
            s = 8-x%8 
            screen[i] |= ((1<<s) - 1) << (x%8)
            t = y%8+1
            screen[j] |= (1<<t) - 1
            for k in range(i+1, j):
                screen[k] = 255
        return screen


