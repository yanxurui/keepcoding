from collections import defaultdict

class FindPair:
    def countPairs(self, A, n, tsum):
        # write code here
        d = defaultdict(int)
        rst = 0
        for a in A:
            b = tsum-a
            if d[b] > 0:
                rst += d[b]
            d[a] += 1
        return rst


